#include <bits/stdc++.h>
using namespace std;
#define ll long long

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n; cin>>n;
    vector<int>v(n);
    for (int i = 0; i < n; i++)
    {
        cin>>v[i];
    }
    int mx = *max_element(v.begin(),v.end());
    int ans = 0 ;
    for (int i = 0; i < n; i++)
    {
        ans+=mx-v[i];
    }
    cout<<ans<<endl;
    
    
    return 0;
}
